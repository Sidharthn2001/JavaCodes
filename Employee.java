package java_demo;

public class Employee {
	String name;
	Double Salary;
	int age;

	public String getName() {
		return name;
	}

	public Double getSalary() {
		return Salary;
	}

	public int getAge() {
		return age;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setSalary(Double salary) {
		this.Salary = salary;
	}

	public void setAge(int age) {
		this.age = age;
	}
}

